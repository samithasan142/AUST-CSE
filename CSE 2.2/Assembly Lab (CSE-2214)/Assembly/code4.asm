.MODEL SMALL
.STACK 100H
.DATA
CR EQU OAH
LF EQU OAH
MSG1 DB 'ENTER A LOWERCASE LETTER : $'
MSG2 DB 0AH,0DH,'IN UPPERCASE IT IS : '
CHAR DB ?,'$'

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX   
    
    LEA DX,MSG1
    MOV AH,9
    INT 21H  
    
    MOV AH,1
    INT 21H
    SUB AL,20H
    MOV CHAR,AL 
    
    LEA DX,MSG2
    MOV AH,9
    INT 21H
    
    MOV AH,4CH
    INT 21H
MAIN ENDP

END MAIN
    